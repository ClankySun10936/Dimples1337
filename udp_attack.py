# 发送
def send(send_list, target_host, times, target_port: int = 4075, delay: int = 1 ):
    client = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    target_host = get_ip(target_host)
    for times in range(times):
        for i in target_host:
            for abc in send_list:
                payload = pack("%dB" % (len(abc)), *abc)
                client.sendto(payload, (i, target_port))
        if times == 1:
            print("发送成功")
            sys.exit(0)
        else:
            print("第%s次执行完毕" % str(times + 1))
        if times != times - 1:
            sleep(delay)


