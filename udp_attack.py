# 格式化要发送的消息
def format_b4_send(content):
    arr = []
    for ch in content:
        tmp = ''.join(list(map(lambda x: hex(ord(x)), ch)))
        if int(tmp, 16) > 0xff:
            tmp = tmp[2:]
            high = int((tmp[0] + tmp[1]), 16)
            low = int((tmp[2] + tmp[3]), 16)
            arr.append(low)
            arr.append(high)
        else:
            high = 0
            low = int((tmp[2] + tmp[3]), 16)
            arr.append(low)
            arr.append(high)
    return arr


